package com.example.yuanping.feihuaapp.adapter;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.List;

/**
 * Created by yuanping on 1/1/18.
 */

public class RegisterWithPhoneAdapter extends FragmentPagerAdapter {

    List<Fragment> fragments;

    public RegisterWithPhoneAdapter(FragmentManager fm, List<Fragment> fragments) {
        super(fm);
        this.fragments = fragments;
    }

    @Override
    public Fragment getItem(int position) {
        return fragments.get(position);
    }

    @Override
    public int getCount() {
        return fragments.size();
    }
}
